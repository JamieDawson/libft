#include "libft.h"

int ft_strnequ(const char *s1, const char *s2, size_t n)
{
     while (*s1 && *s2 && *s1 == *s2 && n)
      {
		  if(*s1 != *s2)
			  return (0);
          s1++;
          s2++;
		  n--;
      }
     return (1);
}
